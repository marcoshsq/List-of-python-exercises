"""Operadores de Comparação

Os operadores de comparação são usados para comparar dois valores.
"""


def test_comparison_operators():
    """Operadores de comparação"""

    # Igual.
    number = 5
    assert number == 5

    # Diferente.
    number = 5
    assert number != 3

    # Maior que.
    number = 5
    assert number > 3

    # Menor que.
    number = 5
    assert number < 8

    # Maior ou Igual que
    number = 5
    assert number >= 5
    assert number >= 4

    # Menor ou Igual que
    number = 5
    assert number <= 5
    assert number <= 6


